console.log(typeof "Hello"); //this will print "string" to the console because the typeof operator returns the type of the operand, which in this case is a string.
console.log(typeof 42); //this will print "number" to the console because the typeof operator returns the type of the operand, which in this case is a number.
console.log(typeof 42.8); //this will print "number" to the console because the typeof operator returns the type of the operand, which in this case is a number (JavaScript does not differentiate between integers and floating-point numbers).
console.log(typeof true);
//type of undefined is undefined
//type of null is object
